//                 S.O.L.I.D

// S - Single Responsibility Principle SRP
/*
A class should have one and only one 
reason to change, meaning that a class should have
only one job
*/


// O - Open Closed Principle OCP
/*
Objects or entities should be open for 
extension but closed for modification
*/

// L - Liskov substitution Principle
/*
Let q(x) be a property provable about objects
of x of Type T. then q(y) should be provable
for objects of y of type S where 
S is a subtype of T
*/

// I - Interface Segregation Principle ISP
/*
A client should never be forced to implement
 an interface that it doesn't use or clients
shouldn't be forced to implement
methods they do not use
*/


// D - Dependency Inversion Principle
/*
Entities must depend on abstractions not on concretions.
It states that the high level module must 
not depend on the low level module 
but they should depend on abstractions
*/